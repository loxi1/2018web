package clases.ventas.celulares;

/**
 *
 * @author LAB-USR-AQ265-A0804
 */
public class Celuar {
    String marca;
    String modelo;
    String costo;
    String cantidda;

    public String getMarca() {
        return marca;
    }

    public void setMarca(String marca) {
        this.marca = marca;
    }

    public String getModelo() {
        return modelo;
    }

    public void setModelo(String modelo) {
        this.modelo = modelo;
    }

    public String getCosto() {
        return costo;
    }

    public void setCosto(String costo) {
        this.costo = costo;
    }

    public String getCantidda() {
        return cantidda;
    }

    public void setCantidda(String cantidda) {
        this.cantidda = cantidda;
    }
}
